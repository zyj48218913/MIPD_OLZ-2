library(rxode2)
library(nlmixr2)

# 2. 定义Li2018成人PopPK模型（仅制剂#2，严格还原文献公式7/9）
li2018_olz_model_drug2 <- rxode2({
  ## -------------------------------
  ## 群体典型值（文献表2+制剂#2专属公式，无制剂修正）
  ## -------------------------------
  TVKA      <- 2.85     # 吸收速率常数 (h⁻¹)，制剂#2默认值
  TVCL      <- 25.4     # CL/F典型值 (L/h)，制剂#2无修正
  TVVC      <- 2390     # 中央室表观分布容积 (L)，制剂#2无修正
  TVQ       <- 8.41     # 表观室间清除率 (L/h)
  TVVP      <- 168      # 周边室表观分布容积 (L)
  ALAG1     <- 0.877    # 吸收延迟时间 (h)，制剂#2通用
  WT_REF    <- 60.59    # 体重标度参考值 (kg)，文献公式5-7统一参考值
  
  ## -------------------------------
  ## 协变量效应系数（仅保留体重对Vc/F的影响，文献表2）
  ## -------------------------------
  B_WT_VC_EXP <- 0.579  # 体重对Vc/F的异速生长指数
  
  ## -------------------------------
  ## 个体参数（仅制剂#2，无其他制剂效应）
  ## 协变量说明：WT=体重 (kg)
  ## -------------------------------
  # 1. 吸收速率常数（Ka）：群体典型值 + 个体间变异
  KA <- TVKA * exp(eta_KA)
  
  # 2. 表观清除率（CL/F）：群体典型值 + 个体间变异 + 与Vc/F的相关性
  CL <- TVCL * exp(eta_CL)
  
  # 3. 中央室表观分布容积（Vc/F）：体重标度 + 群体典型值 + 个体间变异 + 与CL/F的相关性
  Vc <- TVVC * (WT / WT_REF)^B_WT_VC_EXP * exp(eta_Vc)
  
  # 4. 表观室间清除率（Q/F）：群体典型值，无个体间变异（文献表2标注FIXED）
  Q <- TVQ
  
  # 5. 周边室表观分布容积（Vp/F）：群体典型值 + 个体间变异
  Vp <- TVVP * exp(eta_Vp)
  
  ## -------------------------------
  ## 房室微分方程（两室模型+一级吸收+吸收延迟）
  ## -------------------------------
  d/dt(depot)  <- -KA * depot  # 吸收室（含ALAG1延迟）
  d/dt(cent)   <- KA * depot - (CL / Vc) * cent - (Q / Vc) * cent + (Q / Vp) * periph  # 中央室
  d/dt(periph) <- (Q / Vc) * cent - (Q / Vp) * periph  # 周边室
  
  ## -------------------------------
  ## 浓度转换（模型内mg/L → 对外ng/mL）
  ## -------------------------------
  C_mgL <- cent / Vc       # 模型内部浓度单位 (mg/L)
  C_ngmL <- C_mgL * 1000  # 对外输出单位 (ng/mL，1 mg/L = 1000 ng/mL)
})

# 3. 定义个体间变异矩阵（OMEGA，仅保留制剂#2相关参数，严格还原文献表2）
# 计算逻辑：CV% → 方差=log((CV/100)^2 + 1)，保留CL/F与Vc/F的相关性
li2018_omega_drug2 <- lotri({
  # 单独IIV项（仅制剂#2，参数IIV与制剂无关，直接沿用文献估计值）
  eta_KA  ~ log((88.2/100)^2 + 1)  # IIV-Ka：CV=88.2% → 方差≈0.605
  eta_Vp  ~ log((52.4/100)^2 + 1)  # IIV-Vp/F：CV=52.4% → 方差≈0.275
  # CL/F与Vc/F的相关性（block结构，文献直接给出协方差=0.174）
  eta_CL + eta_Vc ~ c(
    log((49.1/100)^2 + 1),  # IIV-CL/F：CV=49.1% → 方差≈0.241
    0.174,                  # 协方差：ωcov CL/F-Vc/F=0.174
    log((40.8/100)^2 + 1)   # IIV-Vc/F：CV=40.8% → 方差≈0.166
  )
  # Q/F无IIV（文献表2标注FIXED，与制剂无关）
  eta_Q ~ 0
})

# 4. 定义残差模型（组合误差：比例+加性，文献表2直接提取，与制剂无关）
li2018_sigma_drug2 <- lotri({
  prop_err ~ 0.216  # 比例误差SD=0.216
  add_err  ~ 0.303  # 加性误差SD=0.303 ng/mL
})