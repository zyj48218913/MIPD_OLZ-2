library(rxode2)
library(nlmixr2)

# 2. 定义Zang2021 PopPK模型（rxode2格式）
zang2021_olz_model <- rxode2({
  ## -------------------------------
  ## 群体典型值（TV=Typical Value，文献表3 Final model）
  ## -------------------------------
  KA   <- 0.30        # 吸收速率常数 (h⁻¹)
  TVCL <- 12.88       # CL/F典型值 (L/h，女性/不吸烟/无协变量)
  TVV  <- 754.41      # V/F典型值 (L)
  
  ## -------------------------------
  ## 协变量系数（B=Beta，文献表3参数估计值，原始对数尺度）
  ## -------------------------------
  B_SEX <-  0.21      # 性别（男性=1）：exp(0.21)=1.23倍
  B_SMK <-  0.21      # 吸烟（吸烟=1）：exp(0.21)=1.23倍
  B_INF <- -0.29      # 感染（感染=1）：exp(-0.29)=0.75倍
  B_VAL <-  0.21      # 丙戊酸钠（使用=1）：exp(0.21)=1.23倍
  B_PER <- -0.25      # 奋乃静（使用=1）：exp(-0.25)=0.78倍
  B_SER <-  0.15      # 舍曲林（使用=1）：exp(0.15)=1.16倍
  B_FLU <- -0.36      # 氟伏沙明（使用=1）：exp(-0.36)=0.70倍
  B_DGH <-  0.70      # 当归龙荟片（使用=1）：exp(0.70)=2.01倍
  
  ## -------------------------------
  ## 个体参数（含协变量效应+个体间变异）
  ## 协变量编码：1=有/是/男性，0=无/否/女性
  ## -------------------------------
  CL <- TVCL * exp(
    B_SEX * SEX +    # 性别效应
    B_SMK * SMK +    # 吸烟效应
    B_INF * INF +    # 感染效应
    B_VAL * VAL +    # 丙戊酸钠效应
    B_PER * PER +    # 奋乃静效应
    B_SER * SER +    # 舍曲林效应
    B_FLU * FLU +    # 氟伏沙明效应
    B_DGH * DGH +    # 当归龙荟片效应
    eta_CL           # CL/F个体间变异（指数模型）
  )
  
  V <- TVV * exp(eta_V)  # V/F个体间变异（指数模型）
  
  ## -------------------------------
  ## 房室微分方程（一级吸收+一级消除）
  ## -------------------------------
  d/dt(depot) <- -KA * depot  # 吸收室
  d/dt(cent)  <-  KA * depot - (CL / V) * cent  # 中央室
  
  ## -------------------------------
  ## 浓度转换（模型内mg/L → 对外ng/mL）
  ## -------------------------------
  C_mgL <- cent / V       # 模型内部浓度单位 (mg/L)
  C_ngmL <- C_mgL * 1000  # 对外输出单位 (ng/mL，1 mg/L = 1000 ng/mL)
})

# 3. 定义个体间变异矩阵（OMEGA，指数模型CV转方差的正确公式）
zang2021_omega <- lotri({
  eta_CL ~ log((9.76/100)^2 + 1)  # CL/F IIV：CV=9.76% → 方差=0.00951
  eta_V  ~ log((15.32/100)^2 + 1) # V/F IIV：CV=15.32% → 方差=0.02298
})

# 4. 定义残差模型（比例误差，文献指定SD=0.20）
zang2021_sigma <- lotri({
  prop_err ~ 0.20  # 比例残差误差SD=0.20（lotri直接填SD，自动计算方差）
})